# Comando set OBS: Em Python, os conjuntos (set) são estruturas de dados não ordenadas.

# usando set
numeros = ([1,2,3,4,5,1,5,6])
print(set(numeros))

fruta = set('abacaxi')
print(fruta)

carros = set(('celta','gol','palio','gol','palio','onix',))
print (carros)

# usando {}
linguagens = {'python','python','java','css'}
print(linguagens)
